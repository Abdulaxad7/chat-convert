package com.example.demo.auth;

/**
 * This class represents a holder for a token. It provides a static field to store the token value.
 *
 * <p>The token value can be accessed and modified using the {@code TOKEN} field.</p>
 *
 * <p><b>Note:</b> This class is declared as {@code final} to prevent inheritance.</p>
 */
public final class TokenHolder {
    public static String TOKEN;
}
